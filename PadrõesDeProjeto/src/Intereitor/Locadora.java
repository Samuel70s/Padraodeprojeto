package Intereitor;

public class Locadora {
 private String nomeFilme;
private double preco;

public void setNomeFilme(String nome) {
	this.nomeFilme = nome;
}

public void setPreco(double preco) {
	this.preco = preco;
}

public String getNomeFilme() {
	return nomeFilme;
}

public double getPreco() {
	return preco;
}

}
